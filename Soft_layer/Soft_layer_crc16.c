unsigned short crc_ccitt(unsigned char *ptr,
					   unsigned int len)

{
  unsigned short us_crc;
  unsigned char da;

  us_crc=0;
  while(len--!=0)
  {
    da=(unsigned char) (us_crc >> 8);  
    us_crc <<= 8;              		     
    us_crc^=crcccitt_tab[da^*ptr];      
    ptr++;
  }
  return(us_crc);
}

