unsigned short crc16(unsigned char *data,unsigned int length)
{
    unsigned char uchCRCHi = 0xff ; 
    unsigned char uchCRCLo = 0xff ; 
    unsigned int  uIndex ; 
  
    /*boundary protection  do not change small then 2k(TCP)*/
    if(length>200000)
    {
      return 0;
    }
    
    while (length--)
    {
        uIndex = uchCRCLo ^ *data++ ; 
        uchCRCLo = uchCRCHi ^ (crc16_tab[uIndex]>>8) ;
        uchCRCHi = crc16_tab[uIndex]&0xff ;
    }
    return (uchCRCHi | uchCRCLo<<8) ;
}


